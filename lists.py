# Demos of lists in Python

fav_movies = ["Gladiator", "E.T", "The Way, Way Back", "Braveheart"]

print(fav_movies)

fav_movies.append("When Harry Met Sally") # adds this to the end of list

print(fav_movies)

for movie in fav_movies: # prints all items in list
    print(movie)

fav_movies.insert(2, "R.V.") # inserts this into the 3rd position

print(fav_movies)

del(fav_movies[-1]) # deletes last item in list

print(fav_movies)



